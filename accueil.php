    <h1 class="titre-encadre">Le métier d'ingénieur d'études et de développement</h1>
    <p>
        L'ingénieur d'études et de développement (plus connu sous le nom d'analyste-programmeur) est chargé d'analyser la demande du client, de créer soi même, dans certain cas, <a href="/lexique.php?query=<?=urlencode('cahier des charges')?>" title="aller voir au lexique" class="lien-lexique">le cahier des charges</a> ou de le suivre, et de développer des logiciels. Il est aussi chargé de la maintenance d'anciens logiciels.<br />
        Le majorité des objets électroniques conçus de nos jours ont dans leur mémoire un programme, qui à été écrit par un analyste-programmeur. <br />
        Sans les programmeurs informatiques, les objets connectés tels que nous les connaissons aujourd'hui (ordinateurs, cellulaires...) et les services qui y sont associés (moteurs de recherche, GPS, jeux vidéos...) n'aurraient pû exister. <br />
        Il existe plusieurs types de développeurs, ont peut citer quatres grandes classes :
    </p>
    <ul>
    <li>Les développeurs webs : créent des sites internet comme outlook, youtube etc.</li>
    <li>Les développeurs logiciels : créent des logiciels pour ordinateur, comme Google Chrome, Microsoft Word etc.</li>
    <li>Les développeurs mobiles : créent des applications mobiles comme Google GO, Snapchat etc.</li>
    <li>Les développeurs IA : ils développent et font des recherches sur des <a href="/lexique.php?query=<?=urlencode('intelligence artificielle')?>" title="aller voir au lexique" class="lien-lexique">intelligence artificielles</a>, comme dans la Google car, ou comme le fameux <a href="/lexique.php?query=<?=urlencode('tay AI')?>" title="aller voir au lexique" class="lien-lexique">Tay AI</a> etc.</li>
    </ul>
    <p>
    Il existe de nombreux autres types de développeurs, comme les développeurs d'outils ou de moteurs de jeux, mais ils sont moins nombreux.
    </p>
    <h2 class="sous-titre-encadre">Histoire</h2>
    <p>
        Avant l'arrivée des <a href="/lexique.php?query=<?=urlencode('CMS')?>" title="aller voir au lexique">CMS</a>, des <a href="/lexique.php?query=<?=urlencode('progiciel')?>" title="aller voir au lexique" class="lien-lexique">progiciels</a>, trois employés travaillaient ensemble autour du projet : le chef de projet, le programmeur, et l'analyste. Le chef de projet était chrgé de gérer les contacts avec le client, de gérer l'équipe et d'établir le cahier des charges. Après le perfectionnement des ordinateurs dans les années 1985, le programmeur à fusionné avec
        l'analyste, pour des raisons budgétaires, mais aussi parce que le gain de temps qu'apportait un analyste était devenu trop faible.
        Il existe aussi des programeurs en chef, qui remplace partiellement ou totalement le chef de projet, en fonction de la charge de travail.
    </p>
