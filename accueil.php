    <h2 class="sous-titre-encadre">L'éditeur de texte</h2>
    <p>
        Où les programmeur écrivent-ils leurs codes ? Pour les <a href="/lexique.php?query=<?=urlencode('langage interprété')?>" title="aller voir au lexique" class="lien-lexique">langages interprétés</a>, c'est vers l'éditeur de texte ou de code que l'on va se tourner. <br />
        L'éditeur de texte, comme le bloc-notes, préinstallé sur la majorité des ordinateurs personnels. permet d'écrire du texte brut dans un fichier. Il ne faut pas confondre l'éditeur de texte avec le traitement de texte, qui, lui, met en forme le texte et ajoute d'autres données, comme des images.
        L'éditeur de code, comme visual studio code (sur lequel est programmé ce site, photo juste en dessous), rempli les mêmes fonctions que le bloc-notes, mais avec des fonctionnalités propice au développement.
    </p>
    <img src="/images/corps/editeur-de-texte.png" alt="photo d'un éditeur de texte" class="image-encadre-moyenne" />
    <h2 class="sous-titre-encadre">L'IDE (ou EDI)</h2>
    <p>
        L'IDE, ou EDI en français (pour Environnement de Développement Intégré) permet de programmer cette fois ci avec des <a href="/lexique.php?query=<?=urlencode('langage compilé')?>" title="aller voir au lexique" class="lien-lexique">langages de programmation compilés</a>. Il remplissent les mêmes fonction que l'éditeur de code, mais un <a href="/lexique.php?query=<?=urlencode('compilateur')?>" title="aller voir au lexique" class="lien-lexique">compilateur</a> et un éditeur de lien y sont directement intégrés et peuvent être lancés en une seule pression sur un bouton.
    </p>
