    <h2 class="sous-titre-encadre">Qualités et compétences requises</h2>
    <p>
        La machine n'est pas intelligente, c'est un fait; un analyste-programmeur doit donc être très minutieux, car une seul petite erreur peut entrainer d'énormes conséquences, la machine ne comprendra pas ce que vous voulez faire, et fait ce que vous lui dites de faire. <br /><br />
        Le programmeur doit aussi avoir de bonnes capacités d'analyses, pour comprendre la demande du client et lui proposer le meilleur résultat. Il doit être doté d'une bonne logique, de rigueur, et de précision. <br />
        Il doit aussi maitriser la langue anglaise, qui est la langue principale dans ce domaine, les langages de programmation comporte des mots uniquement anglais, et leurs créateurs mettent à disosition une <a href="/lexique.php?query=<?=urlencode('documentation d\'un langage de programmation')?>" title="aller voir au lexique" class="lien-lexique">documentation</a>, souvent en anglais. Aussi, les plus grandes entreprises nécéssitant le plus de prorammeurs sont états-uniennes, donc les chances de devoir parler à des anglophones sont accrues (google, amazon, facebook...).
    </p>
    <img src="/images/corps/siege-google.jpg" alt="Le siège social de google" class="image-encadre-moyenne">
    <span class="legende-image">Le siège social de google à montain view (californie)</span>
    <p>
        En programmation, il faut bien évidemment avoir des connaissances en informatiques, pour puvoir résoudre des problèmes, en <a href="/lexique.php?query=<?=urlencode('algorithmique')?>" title="aller voir au lexique" class="lien-lexique">algorithmique</a>, pour pouvoir imaginer une structure pour son programme, mais aussi, selon le secteur d'activité, en mathématiques.
    </p>
    <h2 class="sous-titre-encadre">Les tâches fréquentes</h2>
    <p>
        Comme dans la majorité des métiers, il y a des tâches à accomplir fréquemment. On peut citer (dans l'ordre de chronologique):
        <ol>
            <li>L'analyse des besoins du client. Tâche très fréquente, essentielle à la définition des attentes du client.</li>
            <li>Écriture d'un cahier des charges (dépend du poste et de l'ampleur du projet).</li>
            <li>Conception et programmation du projet (tâche la plus fréquente).</li>
            <li>Réaliser des test, des <a href="/lexique.php?query=<?=urlencode('chasse aux bogues')?>" title="aller voir au lexique" class="lien-lexique">chasses aux bogues</a> (tâche peu fréquente, souvent réalisé par des chasseurs de bogues).</li>
            <li>Maintenance d'un produit (relativement fréquent).</li>
            <li>Rédiger des notices d'utilisation ou des guides (peu fréquent, les logiciels sont souvent simple d'utilisation aujourd'hui).</li>

        </ol>
    </p>
